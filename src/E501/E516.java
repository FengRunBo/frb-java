package E501;

import java.util.Random;

public class E516 {

	public static void main(String[] args) {
		// TODO 自动生成的方法存根
		Random r=new Random();
		System.out.println("生成 boolean 类型的随机数："+r.nextBoolean());
		System.out.println("生成  double类型的随机数："+r.nextDouble());
		System.out.println("生成  float类型的随机数："+r.nextFloat());
		System.out.println("生成  int类型的随机数："+r.nextInt());
		System.out.println("生成  0到10之间int类型的随机数："+r.nextInt(10));
		System.out.println("生成  long类型的随机数："+r.nextLong());
		
	}

}
